// Type definition for tracking used questions
interface UsedQuestions {
  easy: number[];
  medium: number[];
  hard: number[];
}

interface Question {
  question: string;
  options: string[];
  correctAnswer: number;
  explanation: string;
  originalIndex: number;
}

type Difficulty = 'Fácil' | 'Médio' | 'Difícil';

export const getQuestionsForPattern = (
  pattern: Difficulty[],
  usedQuestions: UsedQuestions
): Question[] => {
  const questions: Question[] = [];

  for (let i = 0; i < pattern.length; i++) {
    const difficulty = pattern[i];
    let availableQuestions: Question[];

    if (difficulty === 'Fácil') {
      availableQuestions = easyQuestions.filter(q => !usedQuestions.easy.includes(q.originalIndex));
    } else if (difficulty === 'Médio') {
      availableQuestions = mediumQuestions.filter(q => !usedQuestions.medium.includes(q.originalIndex));
    } else {
      availableQuestions = hardQuestions.filter(q => !usedQuestions.hard.includes(q.originalIndex));
    }

    if (availableQuestions.length > 0) {
      const randomIndex = Math.floor(Math.random() * availableQuestions.length);
      questions.push(availableQuestions[randomIndex]);
    } else {
      console.warn(`No more available ${difficulty} questions. Consider resetting used questions.`);
      return [];
    }
  }

  return questions;
};
